package printingdasar_java;

/**
 *
 * @author RizkyKhapidsyah
 */
public class PrintingDasar_Java {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        System.out.println("Nama Kamu Siapa?");         //println   = mencetak tulisan ke layar + enter
        System.out.print("Nama Saya Anu \n");           //print     = mencetak tulisan ke layar tanpa enter. \n = perintah untuk menambahkan enter
        System.out.printf("Umur Saya %d tahun.", 90);       //printf    = disebut juga 'print format': mencetak tulisan ke layar dengan menggunakan assigment format. (%d = decimal)
    }
}
